void solve()
{
    vector<bool> letters(26, false);
    int ans = 0;
    for (int i = 0; i < 4; i++)
    {
        char a;
        cin >> a;
        a -= 'a';
        if (letters[a] == false)
        {
            letters[a] = true;
            ans++;
        }
    }
    cout << ans - 1 << endl;
}
