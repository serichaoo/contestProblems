void solve()
{
    int n;
    int count = 0;
    cin >> n;
    vi stds(n);
    for (int i = 0; i < n; i++)
    {
        cin >> stds[i];
        if (i + 1 == stds[i])
            count++;
    }
    cout << ((count + 1) / 2) << endl;
}
